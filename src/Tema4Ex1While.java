public class Tema4Ex1While {
    public static void main(String[] args) {

        //Scrie un program care afiseaza numerele de la 1 la n, n fiind citit de la tastatura

        int n = 10;
        int i = 1;
        while (i <= n) {
            System.out.println(i);
            i++;

        }
    }
}
